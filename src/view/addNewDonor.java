    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

if (D_Fname.getText().isEmpty() || D_Lname.getText().isEmpty() || D_address.getText().isEmpty() || D_number.getText().isEmpty() || D_age.getText().isEmpty())
         JOptionPane.showMessageDialog(this, "Enter required field(s) then continue", "Missing field(s)", JOptionPane.ERROR_MESSAGE);
else{
        int donor = Integer.parseInt(D_ID.getText());
        String Fname = D_Fname.getText();
        String Lname = D_Lname.getText();
        String address = D_address.getText();
        String number = D_number.getText();
        int age = Integer.parseInt(D_age.getText());
        String gender = (String) D_Gender.getSelectedItem();
        String Bloodtype = (String) BloodType.getSelectedItem();
        String BloodDraw = BDD.getText();
        int B = Integer.parseInt(BQ.getText());
        int bc = Integer.parseInt(BC.getText());

        new Controller().addDonor(donor, Fname, Lname, age, gender, address, number);
        new Controller().addBlood(donor, B, bc, Bloodtype, BloodDraw);

        
}   
    }//GEN-LAST:event_jButton2ActionPerformed

